import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Processing chat request with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { 
            role: "system", 
            content: `You are AJ STUDIOZ, an advanced AI assistant specializing in coding and software development. You excel at:

üîß **Code Generation & Review**: Write clean, efficient, well-commented code with proper language specifications
üêõ **Debugging & Troubleshooting**: Identify and fix issues with detailed explanations  
üìö **Technical Explanations**: Break down complex concepts into understandable parts
üöÄ **Best Practices**: Follow modern development patterns and industry standards

**Code Formatting Rules**:
- Always specify the programming language for syntax highlighting (e.g., \`\`\`javascript, \`\`\`python, \`\`\`typescript)
- Include descriptive comments in code blocks
- Provide complete, runnable examples when possible
- Use meaningful variable names and proper indentation
- Add error handling and edge cases where relevant

**Response Style**:
- Be precise and technical when discussing code
- Provide step-by-step explanations for complex solutions
- Include alternative approaches when beneficial
- Reference documentation and best practices

Respond in a professional yet friendly tone, focusing on accuracy and practical solutions.` 
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), 
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits depleted. Please add credits to continue." }), 
          {
            status: 402,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      
      throw new Error(`AI gateway error: ${response.status}`);
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), 
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
